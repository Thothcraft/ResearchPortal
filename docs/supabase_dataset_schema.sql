-- Dataset/file schema fallback for Supabase Postgres.
-- Run this only if your deployed database is missing these tables/columns.

create table if not exists public.training_dataset (
  id integer generated by default as identity primary key,
  user_id integer not null references public.user_account(user_id) on delete cascade,
  name varchar(255) not null,
  description text,
  created_at timestamp without time zone default now(),
  updated_at timestamp without time zone default now()
);

create table if not exists public.file_device_update (
  update_id integer generated by default as identity primary key,
  file_id integer not null references public.file(file_id) on delete cascade,
  device_id integer not null references public.device(device_id) on delete cascade,
  updated_at timestamp without time zone default now(),
  file_hash text
);

create table if not exists public.device_file (
  id integer generated by default as identity primary key,
  device_id integer not null references public.device(device_id) on delete cascade,
  user_id integer not null references public.user_account(user_id) on delete cascade,
  filename text not null,
  size bigint,
  file_type text,
  created_at timestamp without time zone,
  modified_at timestamp without time zone,
  on_device boolean default true,
  on_cloud boolean default false,
  cloud_file_id integer references public.file(file_id) on delete set null,
  upload_requested boolean default false,
  last_synced timestamp without time zone default now(),
  constraint uq_device_filename unique (device_id, filename)
);

create table if not exists public.dataset_file (
  id integer generated by default as identity primary key,
  dataset_id integer not null references public.training_dataset(id) on delete cascade,
  file_id integer not null references public.file(file_id) on delete cascade,
  label varchar(255) not null,
  created_at timestamp without time zone default now()
);

alter table public.file
  add column if not exists storage_path varchar(500),
  add column if not exists sample_content text,
  add column if not exists data_type varchar(50),
  add column if not exists folder_id integer,
  add column if not exists labels text;

create index if not exists ix_training_dataset_user_id on public.training_dataset(user_id);
create index if not exists ix_training_dataset_created_at on public.training_dataset(created_at);
create index if not exists ix_file_device_update_file_id on public.file_device_update(file_id);
create index if not exists ix_file_device_update_device_id on public.file_device_update(device_id);
create index if not exists ix_device_file_device_id on public.device_file(device_id);
create index if not exists ix_device_file_user_id on public.device_file(user_id);
create index if not exists ix_device_file_filename on public.device_file(filename);
create index if not exists ix_device_file_modified_at on public.device_file(modified_at);
create index if not exists ix_dataset_file_dataset_id on public.dataset_file(dataset_id);
create index if not exists ix_dataset_file_file_id on public.dataset_file(file_id);
create index if not exists ix_dataset_file_label on public.dataset_file(label);
